    private void answerListener(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_answerListener
        optionA.setEnabled(true);
        optionB.setEnabled(true);
        optionC.setEnabled(true);
        optionD.setEnabled(true);
        if (null != answer) {
            switch (answer) {
                case "a":
                    if (optionA.isSelected()) {
                        getQuestion();
                        setAnswers();
                        getCount();
                        plcount++;
                        prizeLadder();
                    } else {
                        if (plcount <= 5) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $5000!");
                        }
                        else if (plcount >= 5 && plcount <= 10) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $50,000!");
                        }
                        else if (plcount >= 10 && plcount <= 15) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You have won the game, you take home $1,000,000!");
                        }
                    }
                    break;
                case "b":
                    if (optionB.isSelected()) {
                        getQuestion();
                        setAnswers();
                        getCount();
                        plcount++;
                        prizeLadder();
                    } else
                    {
                        gamelostFrame.setVisible(true);
                        if (plcount >= 5 && plcount <= 10) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $5000!");
                        }
                        else if (plcount >= 10 && plcount <= 14) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $50,000!");
                        }
                        else if (plcount == 15) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You have won the game, you take home $1,000,000!");
                        }
                    }
                    break;
                case "c":
                    if (optionC.isSelected()) {
                        getQuestion();
                        setAnswers();
                        getCount();
                        plcount++;
                        prizeLadder();
                    } else 
                    {
                        gamelostFrame.setVisible(true);
                        if (plcount <= 5) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $5000!");
                        }
                        else if (plcount >= 5 && plcount <= 10) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $50,000!");
                        }
                        else if (plcount >= 10 && plcount <= 15) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You have won the game, you take home $1,000,000!");
                        }
                    }
                    break;
                case "d":
                    if (optionD.isSelected()) {
                        getQuestion();
                        setAnswers();
                        getCount();
                        plcount++;
                        prizeLadder();
                    } else {
                        gamelostFrame.setVisible(true);
                        if (plcount >= 5 && plcount <= 10) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $5000!");
                        }
                        else if (plcount >= 10 && plcount <= 14) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You answered wrong, but you still take home $50,000!");
                        }
                        else if (plcount >= 15) {
                        gamelostFrame.setVisible(true);
                        gamelostLabel.setText("You have won the game, you take home $1,000,000!");
                        }
                    }
                    break;
                default:
                    break;
            }
        }
    }//GEN-LAST:event_answerListener

