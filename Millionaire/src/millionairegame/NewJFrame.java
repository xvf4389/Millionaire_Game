    private void ataButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ataButtonActionPerformed
        ataFrame.setVisible(true);
        ataButton.setEnabled(false);
        Random ataR = new Random();
        ataA = ataR.nextInt(50) + 1;
        ataB = ataR.nextInt(50) + 1;
        ataC = ataR.nextInt(50) + 1;
        ataD = ataR.nextInt(50) + 1;
        ataT = ataA + ataB + ataC + ataD;

        ataA = (70 / ataT) * ataA;
        ataB = (70 / ataT) * ataB;
        ataC = (70 / ataT) * ataC;
        ataD = (70 / ataT) * ataD;

        NumberFormat formatter = new DecimalFormat("#.00");

        switch (answer) {
            case "a":
                ataA = ataA + 30;
                ataLabel.setText("A = " + formatter.format(ataA) + "%." + ", " + "B = " + formatter.format(ataB) + "%." + ", " + "C = " + formatter.format(ataC) + "%." + ", " + "D = " + formatter.format(ataD) + ", ");
                break;
            case "b":
                ataB = ataB + 30;
                ataLabel.setText("A = " + formatter.format(ataA) + "%." + ", " + "B = " + formatter.format(ataB) + "%." + ", " + "C = " + formatter.format(ataC) + "%." + ", " + "D = " + formatter.format(ataD) + "%.");
                break;
            case "c":
                ataC = ataC + 30;
                ataLabel.setText("A = " + formatter.format(ataA) + "%." + ", " + "B = " + formatter.format(ataB) + "%." + ", " + "C = " + formatter.format(ataC) + "%." + ", " + "D = " + formatter.format(ataD) + "%.");
                break;
            case "d":
                ataD = ataD + 30;
                ataLabel.setText("A = " + formatter.format(ataA) + "%." + "\n" + "B = " + formatter.format(ataB) + "%." + ", " + "C = " + formatter.format(ataC) + "%." + ", " + "D = " + formatter.format(ataD) + "%.");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_ataButtonActionPerformed

